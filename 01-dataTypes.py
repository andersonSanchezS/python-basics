# Fundamental data types in Python

# Integer
integer: int = 10

# Float
decimal: float = 10.8

# Boolean
boolean: bool = True

# String
string: str = "Hello World"

# List
list_of_numbers: list = [1, 2, 3, 4, 5]

# Tuple
tuple_of_numbers: tuple = (1, 2, 3, 4, 5)

# Set
set_of_numbers: set = {1, 2, 3, 4, 5}

# Dictionary
dictionary: dict = {"name": "John", "age": 30}

