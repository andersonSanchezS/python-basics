# Math Functions
import math

# Sum of two numbers - suma
sum_of_two_numbers = 10 + 20

# Difference of two numbers - resta
difference_of_two_numbers = 10 - 20

# Product of two numbers - multiplicacion
product_of_two_numbers = 10 * 20

# Quotient of two numbers - division
quotient_of_two_numbers = 10 / 20

# Remainder of two numbers - modulo
remainder_of_two_numbers = 10 % 20

# Exponent of two numbers - exponente
exponent_of_two_numbers = 10 ** 20

# Floor division of two numbers - division entera
floor_division_of_two_numbers = 10 // 20

# Absolute value of a number - valor absoluto
absolute_value_of_a_number = abs(-10)

# Square root of a number - raiz cuadrada
square_root_of_a_number = math.sqrt(100)

# Round a number - redondeo
round_a_number = round(10.8)

# Ceil a number - redondeo hacia arriba
ceil_a_number = math.ceil(10.8)

# Floor a number - redondeo hacia abajo
floor_a_number = math.floor(10.8)

# Factorial of a number - factorial
factorial_of_a_number = math.factorial(10)

# Modulo of two numbers - modulo
modulo_of_two_numbers = 10 % 2

# Logarithm of a number - logaritmo
logarithm_of_a_number = math.log(10)
