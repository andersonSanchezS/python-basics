# Strings

# Basic string
username: str = "superCoder"
password: str = "superSecret"

# Long string
long_string: str = '''
this is a long string
And it is wrapped in triple quotes
'''

# String concatenation
concatenated_string: str = username + " " + password

# Escape subsequences
escaped_string: str = "it's sunny outside"

# String formatting
formatted_string: str = f'credentials: {username} {password}'

